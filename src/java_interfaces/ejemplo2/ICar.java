package java_interfaces.ejemplo2;

public interface ICar {
    /**
     * Obtiene la marca del carro.
     *
     * @return La marca del carro.
     */
    String getBrand();

    /**
     * Obtiene el modelo del carro.
     *
     * @return El modelo del carro.
     */
    String getModel();

    /**
     * Obtiene el año del carro.
     *
     * @return El año del carro.
     */
    int getYear();

    /**
     * Obtiene el número de puertas del carro.
     *
     * @return El número de puertas del carro.
     */
    int getNumDoors();

    /**
     * Obtiene el color del carro.
     *
     * @return El color del carro.
     */
    String getColor();

    /**
     * Obtiene el precio del carro.
     *
     * @return El precio del carro.
     */
    double getPrice();

}
